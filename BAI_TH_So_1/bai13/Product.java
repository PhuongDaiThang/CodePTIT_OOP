package bai13;

 class Product {
    String productCode;
    String productName;
    int price;
    int warrantyPeriod;

    public Product(String productCode, String productName, int price, int warrantyPeriod) {
        this.productCode = productCode;
        this.productName = productName;
        this.price = price;
        this.warrantyPeriod = warrantyPeriod;
    }
}
